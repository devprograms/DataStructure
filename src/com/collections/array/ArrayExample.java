package com.collections.array;

public class ArrayExample {
	public static void main(String[] args) {
		int abc[][] ={
				{1,2,3,4},
				{4,5,6,7,2,7},
				{8,9,10,11}
		};
		
		for(int i=0; i<3; i++){
			for(int j=0; j<4; j++){
				System.out.print(" "+abc[i][j]);
			}
			System.out.println(" ");
		}
		
	}
	
	//test
	
	

}
